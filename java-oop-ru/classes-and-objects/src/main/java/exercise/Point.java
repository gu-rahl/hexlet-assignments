package exercise;

public class Point {
    private int x;
    private int y;

    // Конструктор
    public Point(int x, int y) {
        this.x = x;
        this.y = y;
    }

    // Метод для получения координаты x
    public int getX() {
        return x;
    }

    // Метод для получения координаты y
    public int getY() {
        return y;
    }
}
